public class Person {
    private int id;
    private String firstName;
    private String lastName;
    private int age;
    private String phone;

    public Person(int id, String firstName, String lastName, int age, String phone) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.phone = phone;
    }
}